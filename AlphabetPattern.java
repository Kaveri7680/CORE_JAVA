// Alphabet pattern
public class AlphabetPattern{
    public static void main(String[] args) {
       
            int count=0;
                    for(char e='a';e<='p';e++){
                        System.out.print(e);
                        count++;
                        if(count==4){
                            System.out.println();
                            count=0;
                        }                        
                    }
                    System.out.println();
                }
    
        }
    
